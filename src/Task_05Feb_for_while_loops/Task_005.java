package Task_05Feb_for_while_loops;

public class Task_005 {
    //Print multiplication of 5
    public static void main(String[] args) {
        int num=5;
        int Product;
        for(int i=1;i<=10;i++){
            Product=num*i;
            System.out.println(num + "*" + i + "=" +Product);
        }

    }
}
